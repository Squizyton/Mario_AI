        /// <summary>
        /// Allows the game to run logic such as updating the world,
        /// checking for collisions, gathering input, and playing audio.
        /// </summary>
        /// <param name="gameTime">Provides a snapshot of timing values.</param>
        protected override void Update(GameTime gameTime)
        {
            if (GamePad.GetState(PlayerIndex.One).Buttons.Back == ButtonState.Pressed || Keyboard.GetState().IsKeyDown(Keys.Escape))
                Exit();
            // Console.WriteLine(player.Position);
            // TODO: Add your update logic here


            //---------------------------------------------------------------
            if (isTraining == false)
            {
                if (generationumber == 0)
                {
                    InitMarioAINeuralNetworks();
                }

                else
                {
                    nets.Sort();
                    for (int i = 0; i < populationSize / 2; i++)
                    {
                        nets[i] = new NeuralNetwork(nets[i + (populationSize / 2)], random.Next());

                        nets[i].Mutate();



                    }

                    for (int i = 0; i < populationSize; i++)
                    {
                        nets[i].SetFitness(0f);
                    }


                }
                generationumber++;
                isTraining = true;
                Timer = TimerAmount;
                CreateMarioBodies();
            }





            if (Timer >= 0)
            {
                Timer -= (float)gameTime.ElapsedGameTime.TotalSeconds;
            }
            else if (Timer <= 0)
            {
                isTraining = false;
            }

            //----------------------------------          

            //Add time to timer
            if (Keyboard.GetState().IsKeyDown(Keys.Y))
                {
                TimerAmount = 20;
            }
            if (Keyboard.GetState().IsKeyDown(Keys.U))
            {
                TimerAmount = 5;
            }
            if (Keyboard.GetState().IsKeyDown(Keys.T))
            {
                TimerAmount = 1;
            }






            player.Update(gameTime);
            foreach (CollisionTiles tiles in map.CollisionTiles)
            {
                player.Collision(tiles.Rectangle, map.Width, map.Height);
                camera.Update(player.Position, map.Width, map.Height);
                foreach (Goomba goomba in listGoomba)
                {
                    goomba.Collision(tiles.Rectangle, map.Width, map.Height);
                }

                foreach (MarioAI ai in listMarioAI)
                {
                    ai.Collision(tiles.Rectangle, map.Width, map.Height);
                }


            }

            foreach (Goomba goomba in listGoomba)
            {
                if (player.rectangle.TouchTopOf(goomba.rectangle))
                {
                    goomba.velocity.X = 0;
                    goomba.Dead(Content.Load<Texture2D>("Sprites/DeadGoomba"));
                }
                goomba.Update(gameTime);
            }


            foreach (MarioAI ai in listMarioAI)
            {
                if (ai.Position.Y > 600)
                {
                    MariosKilled--;
                }

                ai.Update(gameTime);
            }

            if (player.Position.Y > 600) StartGame();

            if (MariosKilled == populationSize)
            {
                StartGame();
            }

            base.Update(gameTime);
        }

